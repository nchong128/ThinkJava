public class Date {
    private int year;
    private int month;
    private int day;

    public Date() {
        this.year = 2018;
        this.month = 7;
        this.day = 10;
    }
    
    public Date(int year, int month, int day) {
        this.year = year;
        this.month = month;
        this.day = day;
    }

}